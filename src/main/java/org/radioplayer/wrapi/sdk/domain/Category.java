package org.radioplayer.wrapi.sdk.domain;


public class Category  {

	private String name;
	private String href;
	
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getHref() {
		return href;
	}
	
	public void setHref(String href) {
		this.href = href;
	}
	
}
